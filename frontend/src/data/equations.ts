function cnnStages(dataset: Dataset): StageDefinition[] {
  const classes = dataset === "catdog" ? 2 : 10;
  return [
    {
      id: "input",
      name: "Input Image",
      type: "input",
      explanation: "Image tensor enters convolution stack.",
      equations: { primary: "X in R^(C x H x W)" },
      inputShape: dataset === "catdog" ? [3, 64, 64] : [1, 28, 28],
      outputShape: dataset === "catdog" ? [3, 64, 64] : [1, 28, 28],
    },
    {
      id: "preprocessing",
      name: "Normalization",
      type: "preprocessing",
      explanation: "Input tensor normalization before feature extraction.",
      equations: { primary: "x_norm = (x - mu) / sigma" },
      inputShape: dataset === "catdog" ? [3, 64, 64] : [1, 28, 28],
      outputShape: dataset === "catdog" ? [3, 64, 64] : [1, 28, 28],
    },
    {
      id: "conv1",
      name: "Convolution 1",
      type: "conv2d",
      explanation: "Learns local edge/texture detectors via kernels.",
      equations: { primary: "fm(i,j)=sum K * X_patch + b" },
      inputShape: dataset === "catdog" ? [3, 64, 64] : [1, 28, 28],
      outputShape: dataset === "catdog" ? [32, 62, 62] : [16, 26, 26],
      params: { filters: dataset === "catdog" ? 32 : 16, kernelSize: 3 },
    },
    {
      id: "relu_conv1",
      name: "ReLU",
      type: "activation_relu",
      explanation: "Keeps positive feature responses.",
      equations: { primary: "A = max(0, FM)" },
      inputShape: dataset === "catdog" ? [32, 62, 62] : [16, 26, 26],
      outputShape: dataset === "catdog" ? [32, 62, 62] : [16, 26, 26],
    },
    {
      id: "pool1",
      name: "Max Pool",
      type: "max_pool",
      explanation: "Downsamples maps by keeping strongest local responses.",
      equations: { primary: "y(i,j)=max_{2x2}(x)" },
      inputShape: dataset === "catdog" ? [32, 62, 62] : [16, 26, 26],
      outputShape: dataset === "catdog" ? [32, 31, 31] : [16, 13, 13],
    },
    {
      id: "conv2",
      name: "Convolution 2",
      type: "conv2d",
      explanation: "Builds higher-level patterns from previous maps.",
      equations: { primary: "fm2(i,j)=sum_c K_c * FM1_c + b" },
      inputShape: dataset === "catdog" ? [32, 31, 31] : [16, 13, 13],
      outputShape: dataset === "catdog" ? [64, 29, 29] : [32, 11, 11],
      params: { filters: dataset === "catdog" ? 64 : 32, kernelSize: 3 },
    },
    {
      id: "relu_conv2",
      name: "ReLU",
      type: "activation_relu",
      explanation: "Second feature activation stage.",
      equations: { primary: "A2 = max(0, FM2)" },
      inputShape: dataset === "catdog" ? [64, 29, 29] : [32, 11, 11],
      outputShape: dataset === "catdog" ? [64, 29, 29] : [32, 11, 11],
    },
    {
      id: "pool2",
      name: "Max Pool",
      type: "max_pool",
      explanation: "Further reduces spatial dimensions.",
      equations: { primary: "y2(i,j)=max_{2x2}(x2)" },
      inputShape: dataset === "catdog" ? [64, 29, 29] : [32, 11, 11],
      outputShape: dataset === "catdog" ? [64, 14, 14] : [32, 5, 5],
    },
    {
      id: "flatten",
      name: "Flatten",
      type: "flatten",
      explanation: "Converts 3D feature tensor into 1D vector.",
      equations: { primary: "f = flatten(FM)" },
      inputShape: dataset === "catdog" ? [64, 14, 14] : [32, 5, 5],
      outputShape: dataset === "catdog" ? [12544] : [800],
    },
    {
      id: "dense1",
      name: "Dense Layer",
      type: "dense",
      explanation: "Combines spatial features into class evidence.",
      equations: { primary: "z = Wf + b" },
      inputShape: dataset === "catdog" ? [12544] : [800],
      outputShape: [128],
    },
    {
      id: "relu_dense",
      name: "ReLU",
      type: "activation_relu",
      explanation: "Non-linear gating before output.",
      equations: { primary: "a = max(0, z)" },
      inputShape: [128],
      outputShape: [128],
    },
    {
      id: "dense_output",
      name: "Output Dense",
      type: "dense",
      explanation: "Generates logits per class.",
      equations: { primary: "z_out = W_out a + b_out" },
      inputShape: [128],
      outputShape: [classes],
    },
    {
      id: "softmax",
      name: "Softmax",
      type: "softmax",
      explanation: "Final class probability distribution.",
      equations: { primary: "p_i = exp(z_i) / sum_j exp(z_j)" },
      inputShape: [classes],
      outputShape: [classes],
    },
    {
      id: "output",
      name: "Prediction Reveal",
      type: "output",
      explanation: "Class with highest probability wins.",
      equations: { primary: "y_hat = argmax(p)" },
      inputShape: [classes],
      outputShape: [1],
    },
  ];
}

function rnnStages(dataset: Dataset): StageDefinition[] {
  const seq = dataset === "catdog" ? 64 : 28;
  const inDim = dataset === "catdog" ? 192 : 28;
  const classes = dataset === "catdog" ? 2 : 10;
  return [
    {
      id: "input",
      name: "Sequence Input",
      type: "input",
      explanation: "Rows are treated as timesteps and fed sequentially.",
      equations: { primary: "{x_t}_{t=1..T}" },
      inputShape: [seq, inDim],
      outputShape: [seq, inDim],
    },
    {
      id: "preprocessing",
      name: "Normalization",
      type: "preprocessing",
      explanation: "Scales sequence values before recurrent processing.",
      equations: { primary: "x_t = x_t / 255" },
      inputShape: [seq, inDim],
      outputShape: [seq, inDim],
    },
    {
      id: "lstm_t1",
      name: "LSTM Cell T1",
      type: "lstm_cell",
      explanation: "Initial timestep state update.",
      equations: { primary: "h_t, c_t = LSTM(x_t, h_{t-1}, c_{t-1})" },
      inputShape: [inDim],
      outputShape: [128],
      params: { timestep: 1 },
    },
    {
      id: "lstm_tmid",
      name: "LSTM Midpoint",
      type: "lstm_cell",
      explanation: "Mid sequence memory update.",
      equations: { primary: "c_t = f_t*c_{t-1} + i_t*g_t" },
      inputShape: [inDim],
      outputShape: [128],
      params: { timestep: Math.floor(seq / 2) },
    },
    {
      id: "lstm_tfinal",
      name: "LSTM Final",
      type: "lstm_cell",
      explanation: "Final sequence summary hidden state.",
      equations: { primary: "h_T = o_T*tanh(c_T)" },
      inputShape: [inDim],
      outputShape: [128],
      params: { timestep: seq },
    },
    {
      id: "dense_output",
      name: "Output Dense",
      type: "dense",
      explanation: "Maps final hidden state to logits.",
      equations: { primary: "z_out = W_out h_T + b_out" },
      inputShape: [128],
      outputShape: [classes],
    },
    {
      id: "softmax",
      name: "Softmax",
      type: "softmax",
      explanation: "Normalizes logits into class probabilities.",
      equations: { primary: "p_i = exp(z_i) / sum_j exp(z_j)" },
      inputShape: [classes],
      outputShape: [classes],
    },
    {
      id: "output",
      name: "Prediction Reveal",
      type: "output",
      explanation: "Final predicted class.",
      equations: { primary: "y_hat = argmax(p)" },
      inputShape: [classes],
      outputShape: [1],
    },
  ];
}

export function buildPipelineStages(arch: Architecture, dataset: Dataset): StageDefinition[] {
  if (arch === "ANN") return annStages(dataset);
  if (arch === "CNN") return cnnStages(dataset);
  return rnnStages(dataset);
}
